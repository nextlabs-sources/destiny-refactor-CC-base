/**
 * This is the default implementation of the IDefinitionVisitor interface.
 * All of its methods do nothing.
 *
 * @author Sergey Kalinichenko
 */
public class DefaultDefinitionVisitor implements IDefinitionVisitor {

    /**
     * @see IDefinitionVisitor#visitContextType(IContextType)
     */
    public void visitContextType(IContextType contextType) {
        // The default implementation does nothing.
    }

    /**
     * @see IDefinitionVisitor#visitFunctionType(IFunctionType)
     */
    public void visitFunctionType(IFunctionType functionType) {
        // The default implementation does nothing.
    }

    /**
     * @see IDefinitionVisitor#visitObligationType(IObligationType)
     */
    public void visitObligationType(IObligationType obligationType) {
        // The default implementation does nothing.
    }

    /**
     * @see IDefinitionVisitor#visitPolicy(IPolicy)
     */
    public void visitPolicy(IPolicy policy) {
        // The default implementation does nothing.
    }

    /**
     * @see IDefinitionVisitor#visitPolicySet(IPolicySet)
     */
    public void visitPolicySet(IPolicySet policySet) {
        // The default implementation does nothing.
    }

    /**
     * @see IDefinitionVisitor#visitPolicyComponent(IPolicyComponent)
     */
    public void visitPolicyComponent(IPolicyComponent policyComponent) {
        // The default implementation does nothing.
    }

    /**
     * @see IDefinitionVisitor#visitPolicyType(IPolicyType)
     */
    public void visitPolicyType(IPolicyType policyType) {
        // The default implementation does nothing.
    }

    /**
     * @see IDefinitionVisitor#visitFolder(IFolder)
     */
    public void visitFolder(IFolder folder) {
        // The default implementation does nothing.
    }

}
