/**
 * most java native object structure to match the database
 */
public abstract class ReportLog implements DatabaseInsertable{
    public long id;
    
    @Override
    public String toString(){
        return Long.toString(id);
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + (int) (id ^ (id >>> 32));
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        ReportLog other = (ReportLog) obj;
        if (id != other.id)
            return false;
        return true;
    }
}
